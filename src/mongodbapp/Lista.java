    public void populatioTableAnimes()
    {
        
        DefaultTableModel tabAnimes = (DefaultTableModel) listaAnime.getModel();
        
        MongoConnect c = new MongoConnect();
                
        try{
            c.selectAllRecordsFromACollection().forEach((g) -> {
                tabAnimes.addRow(new Object[]{
                    g.getNome(), g.getEpisodios(), g.getTemporadas(), g.getAno()
                });
            });
        }catch(java.lang.NullPointerException e)
        {
            JOptionPane.showMessageDialog(null, "Erro ao popular tabela -> " + e);
        }
        
        
        
    }
    
    
