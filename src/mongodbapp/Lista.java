    private void logarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logarActionPerformed
        // TODO add your handling code here:

        JTextField input1, input2;
        input1 = new JTextField(10);
        input2 = new JTextField(10);
        JPanel myp = new JPanel();
        myp.setLayout(new BoxLayout(myp, BoxLayout.Y_AXIS));
        myp.add(new Label("User: "));
        myp.add(input1);
        myp.add(new Label("Pass: "));
        myp.add(input2);

        int result = JOptionPane.showConfirmDialog(null, myp, "Logar", JOptionPane.OK_CANCEL_OPTION);
        if(result == JOptionPane.OK_OPTION)
        {
            if( input1.getText().equals(user) && input2.getText().equals(pass) )
            {
                JOptionPane.showMessageDialog(null, "Logado");
                populatioTableAnimes();
                logado = true;
                logar.setEnabled(!logado);
                addAnime.setEnabled(logado);
                atualizar.setEnabled(logado);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Senha ou Usuario errados!");
                System.out.println(input1.getText());
                System.out.println(input2.getText());
            }
        }

    }//GEN-LAST:event_logarActionPerformed

    private void addAnimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAnimeActionPerformed
        // TODO add your handling code here:
        
        JTextField input1, input2, input3, input4;
        input1 = new JTextField(15);
        input2 = new JTextField(5);
        input3 = new JTextField(5);
        input4 = new JTextField(5);
        JPanel myp = new JPanel();
        myp.setLayout(new BoxLayout(myp, BoxLayout.Y_AXIS));
        myp.add(new Label("Nome: "));
        myp.add(input1);        
        myp.add(new Label("Episodios: "));
        myp.add(input2);
        myp.add(new Label("Ano: "));
        myp.add(input3);
        myp.add(new Label("Temporada: "));
        myp.add(input4);

        int result = JOptionPane.showConfirmDialog(null, myp, "Adicione seu Anime", JOptionPane.OK_CANCEL_OPTION);
        if(result == JOptionPane.OK_OPTION)
        {

            String[] anime = new String[]{input1.getText(), input2.getText(), input3.getText(), input4.getText()};
            
            for(String a : anime)
            {
                if(a.equals(""))
                {
                    JOptionPane.showMessageDialog(null, "Invalido, apresenta campos vazios!");
                    return;                    
                }                
            }
            
            MongoConnect.connectionToMongoDB(anime);
            
            populatioTableAnimes();
            
        }
        
    }//GEN-LAST:event_addAnimeActionPerformed

